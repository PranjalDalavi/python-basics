

age = "18"
height = "170.5"

# Convert string to integer
age = int(age)

# Convert string to float
height = float(height)

print("Age:", age)
print("Height:", height)

# Integer to float
num = 25
print("Integer to Float:", float(num))

# Float to Integer
pi = 3.14159
print("Float to Integer:", int(pi))

# Integer to String
marks = 95
print("Integer to String:", str(marks))

# Boolean conversion
print("Boolean of 1:", bool(1))
print("Boolean of 0:", bool(0))
print("Boolean of 'Python':", bool("Python"))
print("Boolean of '':", bool(""))
